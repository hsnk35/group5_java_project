package ahmet_codes.week3;

public class SwapNumbers {
    public static void main(String[] args) {

        int a=20;
        int b=10;
        System.out.println("a is = "+a);
        System.out.println("b is = "+b);

        a=a+b;
        b=a-b;
        a=a-b;
        System.out.println("a is = "+a);
        System.out.println("b is = "+b);

    }

}
