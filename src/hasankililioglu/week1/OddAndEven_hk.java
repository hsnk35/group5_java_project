package hasankililioglu.week1;

public class OddAndEven_hk {
    public static void main(String[] args) {

      identify(99);


    }

    public static void identify(int number) {
        if (number % 2 == 0) {
            System.out.println("\"number\" = " + "even");
        }else {
            System.out.println("\"number\" = " + "odd");
        }


    }
}
